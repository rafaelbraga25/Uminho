    private void styleComponents() {
        this.setBorder(new MatteBorder(
                0, 0, 3, 0, Color.decode(ThemeColors.BLUE_COLOR)));
        
        IUC iuc = bFacade.getUCInfo(uc);
        yearLabel.setText(iuc.getAno() + YEAR);
        semesterLabel.setText(iuc.getSemestre() + SEMESTER);
        cuLabel.setText(iuc.getNome());
        
        String str;
        StringBuilder sb = new StringBuilder("");
                
        for (int j = 0; j < shifts.size(); j++) {
            sb.append(shifts.get(j));

            if (j != shifts.size() - 1) {
                sb.append("/");
            }
        }

        str = sb.toString();

        if (str.equals("")) {
            str = NO_SHIFTS;
            modifyButton.setVisible(false);
        }
        
        shiftLabel.setText(str);
        
        if (classesStarted) {
            modifyButton.setVisible(false);
        }
    }
    

